package Parciales.parcialSumDif;

public class sumAndDif {
	private int suma;
	private int dif;

	public sumAndDif(int suma, int dif) {
		this.suma = suma;
		this.dif = dif;
	}

	public int getSuma() {
		return suma;
	}

	public void setSuma(int suma) {
		this.suma = suma;
	}

	public int getDif() {
		return dif;
	}

	public void setDif(int dif) {
		this.dif = dif;
	}
	
	
}
